def area():
    l=float(input("Enter Length for Area: "))
    b=float(input("Enter Breadth for Area : "))
    
    ar=l*b
    
    print(f"Area of Rectangle : {ar}")
    
def perimeter():
    l=float(input("Enter Length for Perimeter: "))
    b=float(input("Enter Breadth for Perimeter : "))
    
    pr=2*(l+b)
    
    print(f"Perimeter of Rectangle : {pr}")
    
    